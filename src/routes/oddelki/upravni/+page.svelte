<p>
    Upravni oddelek je sestavljen iz nadzornega in upravnega odbora.
</p>

<p>
    Člani nadzornega odbora skrbijo
    za pravne, finančne in ostale nujne zadeve ki zagotavljajo normalno delovanje kluba.
    Člani nadzornega odbora so sestavljeni tudi iz zunanjih članov ki nadzirajo da se klub
    vodi v pravo smer in da je kvaliteta posredovanega znanja na najvišjem nivoju.
</p>

